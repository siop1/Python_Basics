a="     python python is fun and i like it fun"
print(a)
##String Functions

print(len(a)) # calculate length of string including whitespace
print(a.endswith("fun")) # return true or false
print(a.count('i')) # shows total number of occurence of any character
print(a.capitalize()) # Capitalize first letter
print(a.find('python')) # finds a word and return index of first occurence of that word
print(a.replace('python','PYTHON')) # replaces all python with PYTHON
print(a.encode())
print(a.strip()) # removes extra spaces

